var navbar = document.getElementById("nsh-header");
var sticky = navbar.offsetTop;

function makeSticky() {
    if (window.pageYOffset > sticky) {
        navbar.classList.add("nsh-sticky");
    } else {
        navbar.classList.remove("nsh-sticky");
    }
}



$(document).ready(function() {
    const openSidebar = $('#nsh-openSidebar');
    const sidebar = $('#nsh-sidebar');
    const navbarLayout = $('#navbar-inner-layout');
    const closeSidebar = $('#nsh-closeSidebar');
    openSidebar.click(function(event) {
        event.stopPropagation();
        sidebar.addClass('openSide');
    });

    closeSidebar.click(function(event) {
        event.stopPropagation();
        sidebar.removeClass('openSide');
    });

    $(document).click(function(event) {
        if (!navbarLayout.is(event.target) && navbarLayout.has(event.target).length === 0) {
            sidebar.removeClass('openSide');
        }
    });

    navbarLayout.click(function(event) {
        event.stopPropagation();
    });
});

