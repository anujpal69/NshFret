$(document).ready(function() {
    const header_search = $('#header_search');
    const update_search = $('#update_search');

    update_search.click(function(event) {
        event.stopPropagation();
        $('#header_search').show();
        $('#update_search').hide();
    })

    $(document).click(function(event) {
        if (!header_search.is(event.target) && header_search.has(event.target).length === 0) {
            $('#header_search').hide();
            $('#update_search').show();
        }
    });
})

